/*
  
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
üí° Problem: Deletion of the Head of Linked List
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
üìù Statement:
Given the head of a singly linked list, delete the head node of the linked list and return the new head.  
The head is the first node of the linked list.

------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
üì• Input:
head -> 1 -> 2 -> 3  

üì§ Output:
head -> 2 -> 3  

------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
üß† Intuition:
- The head node is the first element of the list.  
- To delete it, we just need to move the `head` pointer to `head.next`.  
- If the list is empty or has only one node, return `null`.  

------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
‚ö° Approach:
1. If head is `null` or has no `next`, return `null`.  
2. Otherwise, update head to point to `head.next`.  
3. Return the updated head.  

------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
‚è±Ô∏è Complexity:
- Time: O(1) ‚Üí Only pointer adjustment.  
- Space: O(1) ‚Üí No extra space used.  

------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
üíª Code (Java):
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
*/
class Solution {
    public ListNode deleteHead(ListNode head) {
        // Case 1: Empty list or single node
        if (head == null || head.next == null) {
            return null;
        }
        
        // Case 2: More than one node
        head = head.next;
        return head;
    }
}
