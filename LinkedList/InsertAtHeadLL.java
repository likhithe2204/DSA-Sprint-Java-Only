/*
  
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
üí° Problem: Insertion at the Head of Linked List
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
üìù Statement:
Given the head of a singly linked list and an integer X,  
insert a node with value X at the head of the linked list  
and return the head of the modified list.

------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
üì• Input:
head -> 1 -> 2 -> 3, X = 7  

üì§ Output:
head -> 7 -> 1 -> 2 -> 3  

------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
üß† Intuition:
- To insert at the head, we simply need to:  
  1. Create a new node with the given value.  
  2. Point its `next` to the current head.  
  3. Return the new node as the new head.  

------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
‚ö° Approach:
1. Check if the list is empty ‚Üí if yes, create a new node and return it.  
2. Otherwise, create a new node with value X.  
3. Set newNode.next = head.  
4. Return newNode as the updated head.  

------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
‚è±Ô∏è Complexity:
- Time: O(1) ‚Üí constant-time operation.  
- Space: O(1) ‚Üí no extra space needed.  

------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
üíª Code (Java):
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
*/
class Solution {
    public ListNode insertAtHead(ListNode head, int X) {
        // Create new node with given value
        ListNode newNode = new ListNode(X);
        // Point new node to current head
        newNode.next = head;
        // Return new node as new head
        return newNode;
    }
}
